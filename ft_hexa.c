int	ft_hexa(int num, int maj)
{
	if (num >= 16)
		ft_hexa(num / 16, maj);
	if (!maj)
		ft_putchar("0123456789abcdef"[num % 16]);
	else
		ft_putchar("0123456789ABCDEF"[num % 16]);
	return (1);
}
